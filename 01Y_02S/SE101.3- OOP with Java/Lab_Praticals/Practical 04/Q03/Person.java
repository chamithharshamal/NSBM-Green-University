public class Person {
    private String name;
    private int id;

    public void setname(String name)
    {
        this.name = name;
    }
    public String getname()
    {
        return name;
    }

    public void setId(int id)
    {
        this.id = id ;
    }
    public int getId()
    {
        return id;
    }
}
